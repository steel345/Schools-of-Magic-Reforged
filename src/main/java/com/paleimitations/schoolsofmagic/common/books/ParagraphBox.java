package com.paleimitations.schoolsofmagic.common.books;

public class ParagraphBox {
   public int x;
   public int y;
   public int target;
   public int width;
   public int height;

   public ParagraphBox(int x, int y, int target, int width, int height) {
      this.x = x;
      this.y = y;
      this.target = target;
      this.width = width;
      this.height = height;
   }
}
